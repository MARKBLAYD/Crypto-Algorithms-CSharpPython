# Импортируем необходимые библиотеки
from collections import Counter

# Функция для нахождения простых делителей числа n
def get_prime_divisors(n):
    divisors = []  # Список для хранения простых делителей
    i = 2  # Начинаем с 2, первого простого числа
    while i * i <= n:  # Пока i^2 меньше или равно n
        while n % i == 0:  # Если i делит n
            divisors.append(i)  # Добавляем делитель в список
            n //= i  # Разделяем n на i
        i += 1  # Увеличиваем i для поиска следующих делителей
    if n > 1:  # Если остаток n больше 1, то он тоже является простым делителем
        divisors.append(n)
    # Возвращаем список простых делителей и их кратность
    return [[p, c] for p, c in Counter(divisors).items()]

# Функция для построения таблицы с выражениями g^j mod q для каждого делителя
def make_table(a, q, divisors):
    res = []  # Список для хранения значений
    # Для каждого делителя из списка divisors
    for i in divisors:
        # Для каждого возможного значения j (индекса степени)
        for j in range(i[0]):
            # Вычисляем экспоненту и добавляем в таблицу
            exp = j * ((q - 1) // i[0])
            res.append([i[0], j, int(pow(a, exp, q))])  # [делитель, индекс, значение g^exp mod q]
    return res

# Функция для нахождения дискретного логарифма с использованием таблицы
def find_discrete_log(b, q, a, divisors, table):
    logarithms = {}  # Словарь для хранения найденных логарифмов
    # Для каждого делителя из списка divisors
    for div in divisors:
        val = 0  # Начальная переменная для хранения логарифма
        # Для каждого i из возможных степеней делителя
        for i in range(div[1]):
            if i == 0:
                b_i = b  # Если i == 0, то b_i это просто b
            else:
                # В противном случае, вычисляем новый b_i
                b_i = int(b * pow(a**val, -1, q)) % q  # b_i = b * (a^val)^(-1) mod q

            # Для текущего делителя вычисляем экспоненту
            exp = (q - 1) // (div[0] ** (i + 1))
            # Получаем значение для поиска
            number = pow(b_i, exp, q)

            # Ищем это значение в таблице
            for t in table:
                if number == t[2] and t[0] == div[0]:
                    x_i = t[1]  # Если найдено совпадение, получаем индекс
                    val += x_i * (div[0] ** i)  # Добавляем к текущему значению логарифма
        # Сохраняем логарифм для текущего делителя
        logarithms[div[0] ** (i + 1)] = val
    return logarithms

# Функция для применения китайской теоремы об остатках
def chinese_remainder(a, m):
    M = 1  # Общее произведение всех модулей
    # Для каждого модуля в списке m
    for mi in m:
        M *= mi  # Умножаем на модуль

    x = 0  # Инициализация переменной для вычисления результата
    # Для каждого остатка ai и модуля mi
    for ai, mi in zip(a, m):
        Mi = M // mi  # Вычисляем частное от деления
        # Добавляем вклад в x
        x += ai * Mi * pow(Mi, -1, mi)

    # Возвращаем результат по модулю M
    return x % M

# Основная программа
if __name__ == "__main__":
    # Ввод данных от пользователя
    a = int(input("Введите основание g: "))  # Основание для вычисления логарифма
    b = int(input("Введите число h: "))  # Число, для которого ищется логарифм
    q = int(input("Введите модуль p (простое число): "))  # Модуль p (простое число)

    # Нахождение простых делителей для q-1
    divisors = get_prime_divisors(q - 1)
    print(f'Простые делители q-1: {divisors}')

    # Построение таблицы для поиска значений
    table = make_table(a, q, divisors)
    print(f'Таблица: {table}')

    # Нахождение дискретного логарифма для b
    discr_logs = find_discrete_log(b, q, a, divisors, table)
    mod = list(discr_logs.keys())  # Модули
    rem = list(discr_logs.values())  # Остатки

    print(f'Модули: {mod}\nОстатки: {rem}')

    # Применяем китайскую теорему об остатках для объединения решений
    result = chinese_remainder(rem, mod)

    print(f'Ответ: {result}')
    # Проверка правильности найденного решения
    print(f'Проверка результата : {a**result%q == b}')
